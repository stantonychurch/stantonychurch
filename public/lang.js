function walkAndTranslate(node, isTa) {
    if (node.nodeType === Node.TEXT_NODE) {
        let text = node.nodeValue.trim();
        if (!text) return;
        
        // Store original text
        if (!node.parentElement.hasAttribute('data-orig-text')) {
            // Only store if it's not a script or style
            if (['SCRIPT', 'STYLE'].includes(node.parentElement.tagName)) return;
            node.parentElement.setAttribute('data-orig-text', node.nodeValue);
        }
        
        const origText = node.parentElement.getAttribute('data-orig-text');
        const cleanOrig = origText.trim();
        
        if (isTa && TamilTranslations[cleanOrig]) {
            node.nodeValue = origText.replace(cleanOrig, TamilTranslations[cleanOrig]);
        } else if (!isTa) {
            node.nodeValue = origText;
        }
    } else {
        if (['SCRIPT', 'STYLE'].includes(node.tagName)) return;
        for (let i = 0; i < node.childNodes.length; i++) {
            walkAndTranslate(node.childNodes[i], isTa);
        }
    }
}

function applyFullTranslation(lang) {
    const isTa = lang === 'ta';
    walkAndTranslate(document.body, isTa);
}

// Override applyLang if it exists
window.addEventListener('DOMContentLoaded', () => {
    const origApplyLang = window.applyLang;
    window.applyLang = function() {
        if (origApplyLang) origApplyLang();
        applyFullTranslation(window.currentLang);
    };
    // Initial translation
    setTimeout(() => applyFullTranslation(window.currentLang || localStorage.getItem('lang') || 'en'), 500);
});
